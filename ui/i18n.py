def T(key, **kwargs):
    """Get translated string for current language."""
    lang = st.session_state.get('lang', 'it')
    text = TRANSLATIONS.get(lang, TRANSLATIONS['it']).get(key, key)
    if kwargs:
        text = text.format(**kwargs)
    return text
